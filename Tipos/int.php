<?php
echo 11; // (Valor Literal) Inteiro
echo '<br>';

var_dump(11); // O "Var_Dump" me dá o tipo da variável
echo '<br>';

echo PHP_INT_MAX, '<br>'; // Máximo valor de int
echo PHP_INT_MIN, '<br>'; // Mínimo valor de int

echo 011, '<br>'; // Base Octal (de 0 a 7)

echo 0b1100101010, '<br>'; // Base binária (0 e 1)

echo 0x117acf0, '<br>'; // Base Hexadecimal